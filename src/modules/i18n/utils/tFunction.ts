export function t(str: string, params?: Record<string, any>): string {
  if (ruTranslations[str]) {
    let translated = ruTranslations[str];
    if (params) {
      for (const [key, value] of Object.entries(params)) {
        translated = translated.replace(new RegExp(`{${key}}`, "g"), String(value));
      }
    }
    return translated;
  }
  return txNativeT(str, params);
}
